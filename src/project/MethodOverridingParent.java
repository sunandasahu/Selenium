package project;



public class MethodOverridingParent {
	
	
	
	public void display()
	{
		System.out.println("I am a method in parent class");
	}
	
	

	public static void main(String[] args) {
		 MethodOverridingParent parentObj=new  MethodOverridingParent();
		 parentObj.display();;
		
		

	}

}
